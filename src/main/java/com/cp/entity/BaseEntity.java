package com.cp.entity;

/**
 * 基础的返回值定义
 * 
 * @author zengxm  2014-12-20
 *
 */
public class BaseEntity {
	
	private int ret;
	private String msg;
	
	public int getRet() {
		return ret;
	}

	public void setRet(int ret) {
		this.ret = ret;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}
	
}
